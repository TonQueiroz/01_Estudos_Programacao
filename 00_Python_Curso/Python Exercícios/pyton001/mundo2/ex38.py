'''PROGRAMA QUE COMPARA NUMEROS!

MENOR MAIOR PUU IGUAL!'''


N1 = int(input('digitre o N1?'))

N2 = int(input('Digite o N2?'))

if N1 < N2 :
    print('O NÚMER0', N1 ,' É MENOR QUE O NUMERO ', N2)
elif N1 > N2:
    print ('O NÚMER0', N1 ,' É MAIOR QUE O NUMERO ', N2)
else :
    print ('O NÚMER0', N1 ,' É IGAUAL AO NUMERO ', N2)